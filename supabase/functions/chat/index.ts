import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are KindConnect AI, a friendly and helpful assistant for the KindConnect platform - a charity and volunteer management system connecting donors, volunteers, and NGOs.

Your role is to:
1. **Recommend Causes**: Based on user interests, suggest relevant causes they can donate to (education, healthcare, environment, elderly care, disaster relief, etc.)
2. **Recommend Volunteer Opportunities**: Match users with volunteer opportunities based on their skills, availability, and interests
3. **Answer Questions**: Help users understand how the platform works, how to donate, how to volunteer, and how NGOs can register
4. **Provide Impact Information**: Share how donations and volunteering make a difference

${context ? `Current Context: ${JSON.stringify(context)}` : ''}

Guidelines:
- Be warm, empathetic, and encouraging
- Keep responses concise but helpful (2-3 paragraphs max)
- When recommending causes, explain WHY it might be a good fit
- Encourage users to take action (donate, volunteer, explore)
- If asked about specific NGOs or causes not in context, suggest they browse the Causes or Volunteer pages
- Never make up specific donation amounts or fake statistics

Available categories: Education, Healthcare, Environment, Elderly Care, Disaster Relief, Animal Welfare, Women Empowerment, Child Welfare`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat function error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
